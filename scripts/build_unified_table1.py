"""Shared definitions and aggregate statistics for the GO-BP benchmark at a single
unified metric convention (opt0), all datasets scored by our 10-seed reproduction
(full_metrics_combined_4config.csv).

This module is imported by build_robustness_agg_15ds.py for its dataset lists, method
labels and aggregation helpers, so those cannot drift between the two generators.

Running it directly also emits two combined-table fragments that the paper does not
currently include (it uses the split tables from build_split_tables.py instead):
  - paper/figures/tab_unified_9ds_opt0.tex
  - paper/figures/tab_unified_15ds_opt0.tex
  - aggregate stats (mean rank, never-worst, geom composite, PRISM Welch win/tie/loss) to stdout.
Config = opt0 (best on the 15-dataset benchmark; the beta-faithful config). Numbers are computed,
never hand-entered. NaN (no significant enrichment) renders as "---" and is excluded from ranking.
"""
