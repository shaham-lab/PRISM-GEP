CFG = "o0"
METHODS = ["PRISM", "MALLET", "NMF", "cNMF", "scHPF", "ProdLDA"]
PREF = {"PRISM": f"PRISM{CFG}", "MALLET": f"MALLET{CFG}", "NMF": "NMF", "cNMF": "cNMF",
        "scHPF": "scHPF", "ProdLDA": "ProdLDA"}
PS_VAR = {"PRISM": "PRISM_opt0", "MALLET": "MALLET_opt0", "NMF": "NMF", "cNMF": "cNMF",
          "scHPF": "scHPF", "ProdLDA": "ProdLDA"}
